package shop.Entity;

public class Ourteam {
	private int id;
	private String img;
	private String name;
	private String nickname;
	private String interactively;

	public Ourteam() {
		super();
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getImg() {
		return img;
	}

	public void setImg(String img) {
		this.img = img;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getNickname() {
		return nickname;
	}

	public void setNickname(String nickname) {
		this.nickname = nickname;
	}

	public String getInteractively() {
		return interactively;
	}

	public void setInteractively(String interactively) {
		this.interactively = interactively;
	}

}
